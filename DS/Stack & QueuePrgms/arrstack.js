
let arr = [1, 3, 2];
let arr1 = [5, 4];

let i = arr.length - 1; // index for arr
let j = arr1.length - 1; // index for arr1
let lastIndex = arr.length + arr1.length - 1;

while (j >= 0) {
    if (i >= 0 && arr[i] > arr1[j]) {
        arr[lastIndex] = arr[i];
        i--;
    } else {
        arr[lastIndex] = arr1[j];
        j--;
    }
    lastIndex--;
}

console.log(arr);




































































































































































